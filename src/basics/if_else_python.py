print("This script basically provide simple example of if and else statements in python")

# how to declare a variable
value1 = 10
value2 = 2

print("Value1 is : {}".format(value1))

print("Value2 is : {}".format(value2))


# example of if and else  statement
if value1 > value2:
    print("{} is greater than {}".format(value1,value2))
else:
    print("{} is not greater than {}".format(value1,value2))


